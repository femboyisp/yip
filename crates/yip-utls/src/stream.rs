/// The largest single TLS record plaintext RFC 8446 §5.1 permits.
const MAX_APP_RECORD_LEN: usize = 16384;

/// A generous sanity bound on a single handshake *message* (not record)
/// length while scanning the server's unauthenticated-length-prefixed
/// handshake flight for `Finished` — well above any real
/// EncryptedExtensions/Certificate/CertificateVerify/Finished flight, but
/// far below "attacker can make us buffer gigabytes" territory.
const MAX_HANDSHAKE_MSG_LEN: usize = 1 << 20;

/// Upper bound on the TOTAL decrypted server handshake flight
/// (EncryptedExtensions + Certificate + CertificateVerify + Finished). A real
/// flight is tens of KiB; 256 KiB is generous. Without this, a peer (the outer
/// TLS is zero-server-auth, so this could be a MITM) can stream endless valid
/// non-`Finished` handshake messages and exhaust our memory (review I1).
const MAX_SERVER_FLIGHT_LEN: usize = 256 * 1024;

/// [`RandomSource`] backed by the OS CSPRNG (`getrandom`), for driving
/// [`hello::craft`] in production. [`RandomSource::fill`] cannot itself
/// return a `Result`, so failures are latched and surfaced by
/// [`OsRng::into_result`] after `craft` returns — `craft` only ever
/// transforms already-filled bytes, so a latched failure part-way through
/// still produces *some* `Vec<u8>` (built from bytes that silently stayed
/// zero once RNG failed), but [`connect`] discards it and bails out via
/// `into_result` before ever putting it on the wire.
#[derive(Default)]
struct OsRng {
    error: Option<getrandom::Error>,
}

impl OsRng {
    fn into_result(self) -> Result<(), Error> {
        match self.error {
            Some(e) => Err(Error::Rng(e)),
            None => Ok(()),
        }
    }
}

impl RandomSource for OsRng {
    fn fill(&mut self, buf: &mut [u8]) {
        if self.error.is_some() {
            return;
        }
        if let Err(e) = getrandom::getrandom(buf) {
            self.error = Some(e);
        }
    }
}

/// The same OS-CSPRNG-backed, latched-error `getrandom` bridge as [`OsRng`],
/// but implementing `rand_core::{RngCore, CryptoRng}` instead of
/// [`RandomSource`] — the trait bound `ml_kem::MlKem768::generate` needs
/// (`rand_core::CryptoRngCore`, blanket-implemented for any
/// `RngCore + CryptoRng`). `rand_core::RngCore::fill_bytes` has no `Result`
/// return, so a `getrandom` failure is latched the same way `OsRng` latches
/// one, and [`MlKemRng::into_result`] surfaces it to the caller after
/// `generate` returns (which, on a latched failure, still returns *some*
/// keypair built from bytes that silently stayed zero — [`connect`] discards
/// it and bails out via `into_result` before ever using it).
/// `pub(crate)` (not just private to this module) so [`crate::server`]'s test
/// module can generate a real client ML-KEM keypair the same way `connect`
/// does, without duplicating this bridge.
#[derive(Default)]
pub(crate) struct MlKemRng {
    error: Option<getrandom::Error>,
}

impl MlKemRng {
    fn into_result(self) -> Result<(), Error> {
        match self.error {
            Some(e) => Err(Error::Rng(e)),
            None => Ok(()),
        }
    }
}

impl rand_core::RngCore for MlKemRng {
    fn next_u32(&mut self) -> u32 {
        let mut buf = [0u8; 4];
        self.fill_bytes(&mut buf);
        u32::from_ne_bytes(buf)
    }

    fn next_u64(&mut self) -> u64 {
        let mut buf = [0u8; 8];
        self.fill_bytes(&mut buf);
        u64::from_ne_bytes(buf)
    }

    fn fill_bytes(&mut self, dest: &mut [u8]) {
        if self.error.is_some() {
            return;
        }
        if let Err(e) = getrandom::getrandom(dest) {
            self.error = Some(e);
        }
    }

    fn try_fill_bytes(&mut self, dest: &mut [u8]) -> Result<(), rand_core::Error> {
        self.fill_bytes(dest);
        Ok(())
    }
}

impl rand_core::CryptoRng for MlKemRng {}

/// Builds a 5-byte TLS record header for a `content_type`/`legacy_version`
/// record of plaintext-or-ciphertext length `len`.
fn record_header(
    content_type: u8,
    version: u16,
    len: usize,
) -> Result<[u8; RECORD_HEADER_LEN], Error> {
    let len_u16 = u16::try_from(len).map_err(|_| Error::Protocol("record length exceeds a u16"))?;
    let v = version.to_be_bytes();
    let l = len_u16.to_be_bytes();
    Ok([content_type, v[0], v[1], l[0], l[1]])
}

/// Reads one raw TLS record (5-byte header + body) off `stream`. Returns the
/// outer record type and the (still possibly-encrypted) body.
async fn read_raw_record<S: AsyncRead + Unpin>(stream: &mut S) -> Result<(u8, Vec<u8>), Error> {
    let mut header = [0u8; RECORD_HEADER_LEN];
    stream.read_exact(&mut header).await?;
    let len = usize::from(u16::from_be_bytes([header[3], header[4]]));
    let mut payload = vec![0u8; len];
    stream.read_exact(&mut payload).await?;
    Ok((header[0], payload))
}

/// Scans `hs_flight` (the concatenation of every handshake message decrypted
/// so far from the server's post-`ServerHello` flight) for a complete
/// `Finished` message. Returns the byte offset one past the end of that
/// message on success, or `Ok(None)` if the flight so far ends mid-message
/// (caller should decrypt and append another record and retry).
fn find_finished_end(hs_flight: &[u8]) -> Result<Option<usize>, Error> {
    let mut pos = 0;
    while hs_flight.len() >= pos + 4 {
        let msg_type = hs_flight[pos];
        let len = (usize::from(hs_flight[pos + 1]) << 16)
            | (usize::from(hs_flight[pos + 2]) << 8)
            | usize::from(hs_flight[pos + 3]);
        if len > MAX_HANDSHAKE_MSG_LEN {
            return Err(Error::Protocol(
                "server handshake message exceeds sane length bound",
            ));
        }
        let msg_end = pos + 4 + len;
        if hs_flight.len() < msg_end {
            break;
        }
        if msg_type == HS_TYPE_FINISHED {
            return Ok(Some(msg_end));
        }
        pos = msg_end;
    }
    Ok(None)
}

/// Handshake message type for `EncryptedExtensions` (RFC 8446 §4.3.1).
const HS_TYPE_ENCRYPTED_EXTENSIONS: u8 = 0x08;
/// Handshake message type for `Certificate` (RFC 8446 §4.4.2).
const HS_TYPE_CERTIFICATE: u8 = 0x0b;
/// Handshake message type for `CertificateVerify` (RFC 8446 §4.4.3).
const HS_TYPE_CERTIFICATE_VERIFY: u8 = 0x0f;

/// A generous sanity bound on a single leaf certificate's DER length — real
/// leaf certs are a few KiB; 64 KiB is generous headroom. Bounds
/// [`certificate_leaf_der`]'s allocation against a hostile/MITM peer (the
/// outer TLS is zero-server-auth) claiming an absurd `cert_data` length
/// within the already-bounded server flight — defense in depth alongside
/// [`MAX_SERVER_FLIGHT_LEN`]/[`MAX_HANDSHAKE_MSG_LEN`].
const MAX_LEAF_CERT_DER_LEN: usize = 64 * 1024;

/// `AlertLevel::fatal` (RFC 8446 §6.1, value `2`) — every alert this client
/// ever sends is fatal.
const ALERT_LEVEL_FATAL: u8 = 2;

/// `AlertDescription::bad_certificate` (RFC 8446 §6.2, value `42`) — pinned
/// as the alert a REALITY.4b `CertificateVerify` verification failure sends.
/// This is the RFC-canonical description for "a certificate was corrupt,
/// contained signatures that did not verify correctly, etc." — exactly this
/// client's situation on a pin/signature mismatch — and is documented as
/// what mainstream browsers (Chrome/BoringSSL) send for a TLS 1.3
/// cert-verify failure. This value was NOT captured against a live browser
/// in this environment (no network access + no real cert-mismatch harness
/// available here); REALITY.5's wire-fidelity pass is the place that
/// re-verifies it against a real packet capture and swaps it out if reality
/// disagrees.
const ALERT_DESCRIPTION_BAD_CERTIFICATE: u8 = 42;

/// What REALITY.4b verification needs out of the server's
/// EncryptedExtensions/Certificate/CertificateVerify/Finished flight: the
/// leaf certificate's raw DER, the byte offset in the flight at which the
/// `CertificateVerify` message begins (the RFC 8446 §4.4.3 transcript
/// boundary — everything strictly before this offset is
/// `Transcript-Hash(ClientHello‥Certificate)`), and the `CertificateVerify`
/// message's raw `signature` field.
struct CertFlightInfo {
    leaf_der: Vec<u8>,
    certverify_start: usize,
    signature: Vec<u8>,
}

/// Scans `hs_flight` (see [`find_finished_end`] for its shape — the
/// concatenation of the server's decrypted post-`ServerHello` handshake
/// messages) for the first `Certificate` message and the `CertificateVerify`
/// message, extracting what [`verify_reality_binding`] needs from each.
/// Fail-closed: a missing message, truncated field, or an out-of-bounds
/// value is `Err(Error::RealityVerify(_))`; every access is bounds-checked
/// (`.get`, never direct indexing past a validated point) so hostile bytes
/// cannot panic this.
fn scan_cert_flight(hs_flight: &[u8]) -> Result<CertFlightInfo, Error> {
    let mut pos = 0;
    let mut leaf_der: Option<Vec<u8>> = None;

    while hs_flight.len() >= pos + 4 {
        let msg_type = hs_flight[pos];
        let len = (usize::from(hs_flight[pos + 1]) << 16)
            | (usize::from(hs_flight[pos + 2]) << 8)
            | usize::from(hs_flight[pos + 3]);
        if len > MAX_HANDSHAKE_MSG_LEN {
            return Err(Error::RealityVerify(
                "server handshake message exceeds sane length bound",
            ));
        }
        let body_start = pos + 4;
        let msg_end = body_start + len;
        let Some(body) = hs_flight.get(body_start..msg_end) else {
            break; // not fully buffered — cannot happen on an already-complete flight
        };

        if msg_type == HS_TYPE_CERTIFICATE && leaf_der.is_none() {
            leaf_der = Some(certificate_leaf_der(body)?);
        }
        if msg_type == HS_TYPE_CERTIFICATE_VERIFY {
            let signature = certificate_verify_signature(body)?;
            let leaf_der = leaf_der.ok_or(Error::RealityVerify(
                "server flight is missing a Certificate message",
            ))?;
            return Ok(CertFlightInfo {
                leaf_der,
                certverify_start: pos,
                signature,
            });
        }
        pos = msg_end;
    }

    Err(Error::RealityVerify(
        "server flight is missing a CertificateVerify message",
    ))
}

/// REALITY.5a Task 2: what capturing a dest's server flight needs beyond
/// [`scan_cert_flight`] (which REALITY.4b's verify path already relies on and
/// which this does not alter) — per-message plaintext lengths (each `4 +
/// body_len`, the handshake-message length INCLUDING its own 4-byte header —
/// matching [`crate::template::EncryptedFlightShape`]'s length fields) plus
/// the full cert chain shape (leaf DER + every subsequent chain cert's DER
/// verbatim; see [`CertChainShape`]).
pub struct FlightShapeScan {
    /// The leaf certificate's raw DER (first `CertificateEntry` in the
    /// `Certificate` message's cert list).
    pub leaf_der: Vec<u8>,
    /// The leaf DER's length + every subsequent chain cert's DER, verbatim.
    pub cert_chain: CertChainShape,
    /// `EncryptedExtensions` message length, incl. its 4-byte header.
    pub ee_len: usize,
    /// `Certificate` message length, incl. its 4-byte header.
    pub cert_len: usize,
    /// `CertificateVerify` message length, incl. its 4-byte header.
    pub cert_verify_len: usize,
    /// `Finished` message length, incl. its 4-byte header.
    pub finished_len: usize,
}

/// Scans `hs_flight` (see [`find_finished_end`] for its shape) for
/// REALITY.5a's flight-shape capture: per-message plaintext lengths of
/// EncryptedExtensions/Certificate/CertificateVerify/Finished, plus the
/// leaf certificate's DER and every subsequent chain cert's DER verbatim
/// (see [`certificate_chain_ders`]). Mirrors [`scan_cert_flight`]'s bounded
/// `while hs_flight.len() >= pos + 4` walk over `msg_type ‖ u24 len ‖ body`,
/// but records EVERY message's length rather than stopping at the first
/// `CertificateVerify`. Fail-closed: a missing `Certificate` message,
/// truncated field, or out-of-bounds value is `Err(Error::RealityVerify(_))`;
/// every access is bounds-checked (`.get`, never direct indexing past a
/// validated point) so hostile bytes cannot panic this.
pub fn scan_flight_shape(hs_flight: &[u8]) -> Result<FlightShapeScan, Error> {
    let mut pos = 0;
    let mut leaf_der: Option<Vec<u8>> = None;
    let mut intermediates_der: Vec<Vec<u8>> = Vec::new();
    let mut ee_len = 0usize;
    let mut cert_len = 0usize;
    let mut cert_verify_len = 0usize;
    let mut finished_len = 0usize;

    while hs_flight.len() >= pos + 4 {
        let msg_type = hs_flight[pos];
        let len = (usize::from(hs_flight[pos + 1]) << 16)
            | (usize::from(hs_flight[pos + 2]) << 8)
            | usize::from(hs_flight[pos + 3]);
        if len > MAX_HANDSHAKE_MSG_LEN {
            return Err(Error::RealityVerify(
                "server handshake message exceeds sane length bound",
            ));
        }
        let body_start = pos + 4;
        let msg_end = body_start + len;
        let Some(body) = hs_flight.get(body_start..msg_end) else {
            break; // not fully buffered — cannot happen on an already-complete flight
        };
        let total_len = 4 + len;

        match msg_type {
            HS_TYPE_ENCRYPTED_EXTENSIONS => ee_len = total_len,
            HS_TYPE_CERTIFICATE => {
                cert_len = total_len;
                let (leaf, chain) = certificate_chain_ders(body)?;
                leaf_der = Some(leaf);
                intermediates_der = chain;
            }
            HS_TYPE_CERTIFICATE_VERIFY => cert_verify_len = total_len,
            HS_TYPE_FINISHED => finished_len = total_len,
            _ => {}
        }

        pos = msg_end;
    }

    let leaf_der = leaf_der.ok_or(Error::RealityVerify(
        "server flight is missing a Certificate message",
    ))?;
    let leaf_der_len = leaf_der.len();

    Ok(FlightShapeScan {
        leaf_der,
        cert_chain: CertChainShape {
            leaf_der_len,
            intermediates_der,
        },
        ee_len,
        cert_len,
        cert_verify_len,
        finished_len,
    })
}

/// Extracts every `CertificateEntry`'s raw DER from a `Certificate` message
/// body (RFC 8446 §4.4.2): `context_len(1) ‖ context ‖ list_len(3) ‖
/// [cert_data_len(3) ‖ cert_data ‖ ext_len(2) ‖ ext]…`. Returns
/// `(leaf_der, intermediates_der)` — the FIRST entry is the leaf, every
/// SUBSEQUENT entry is returned verbatim, in chain order, as
/// `intermediates_der`. Fail-closed on truncation, an empty list, or a leaf
/// exceeding [`MAX_LEAF_CERT_DER_LEN`] — this bound is intentionally NOT
/// applied to intermediates (real intermediate CA certs can legitimately run
/// larger than a leaf), which are instead bounded by the already-enforced
/// [`MAX_HANDSHAKE_MSG_LEN`]/[`MAX_SERVER_FLIGHT_LEN`] on the whole
/// message/flight.
fn certificate_chain_ders(body: &[u8]) -> Result<(Vec<u8>, Vec<Vec<u8>>), Error> {
    let ctx_len = usize::from(
        *body
            .first()
            .ok_or(Error::RealityVerify("truncated Certificate message"))?,
    );
    let list_len_start = 1 + ctx_len;
    let list_len_bytes = body
        .get(list_len_start..list_len_start + 3)
        .ok_or(Error::RealityVerify("truncated Certificate message"))?;
    let list_len = (usize::from(list_len_bytes[0]) << 16)
        | (usize::from(list_len_bytes[1]) << 8)
        | usize::from(list_len_bytes[2]);
    let list_start = list_len_start + 3;
    let list_end = list_start + list_len;
    let list = body
        .get(list_start..list_end)
        .ok_or(Error::RealityVerify("truncated Certificate list"))?;

    let mut entries: Vec<Vec<u8>> = Vec::new();
    let mut i = 0;
    while let Some(cd_len_bytes) = list.get(i..i + 3) {
        let cd_len = (usize::from(cd_len_bytes[0]) << 16)
            | (usize::from(cd_len_bytes[1]) << 8)
            | usize::from(cd_len_bytes[2]);
        let cert_start = i + 3;
        let cert_end = cert_start + cd_len;
        let cert_data = list
            .get(cert_start..cert_end)
            .ok_or(Error::RealityVerify("truncated certificate entry"))?;
        let ext_len_bytes = list
            .get(cert_end..cert_end + 2)
            .ok_or(Error::RealityVerify(
                "truncated certificate entry extensions",
            ))?;
        let ext_len = usize::from(u16::from_be_bytes([ext_len_bytes[0], ext_len_bytes[1]]));
        let ext_start = cert_end + 2;
        let ext_end = ext_start + ext_len;
        if ext_end > list.len() {
            return Err(Error::RealityVerify(
                "truncated certificate entry extensions",
            ));
        }
        entries.push(cert_data.to_vec());
        i = ext_end;
    }

    let mut iter = entries.into_iter();
    let leaf = iter
        .next()
        .ok_or(Error::RealityVerify("empty Certificate list"))?;
    if leaf.is_empty() || leaf.len() > MAX_LEAF_CERT_DER_LEN {
        return Err(Error::RealityVerify(
            "leaf certificate length out of bounds",
        ));
    }
    Ok((leaf, iter.collect()))
}

/// Extracts the first `CertificateEntry`'s raw DER (the leaf) from a
/// `Certificate` message body (RFC 8446 §4.4.2): `context_len(1) ‖ context
/// ‖ list_len(3) ‖ [cert_data_len(3) ‖ cert_data ‖ ext_len(2) ‖ ext]…`.
/// Fail-closed on truncation, an empty list, or a leaf exceeding
/// [`MAX_LEAF_CERT_DER_LEN`]. A thin wrapper over
/// [`certificate_chain_ders`] (REALITY.5a), which does the identical walk
/// but also returns any subsequent chain certs; [`scan_cert_flight`] (and,
/// through it, REALITY.4b's verify path) only ever needs the leaf, so it
/// keeps calling this narrower name.
fn certificate_leaf_der(body: &[u8]) -> Result<Vec<u8>, Error> {
    certificate_chain_ders(body).map(|(leaf, _intermediates)| leaf)
}

/// Extracts the `signature` field from a `CertificateVerify` message body
/// (RFC 8446 §4.4.3): `algorithm(2) ‖ signature_len(2) ‖ signature`. The
/// `algorithm` field is intentionally never returned — REALITY.4b hard-pins
/// `ecdsa_secp256r1_sha256` and never reads/trusts the server's announced
/// scheme (see [`verify_certificate_verify`]). Fail-closed on truncation or
/// an empty signature.
fn certificate_verify_signature(body: &[u8]) -> Result<Vec<u8>, Error> {
    let sig_len_bytes = body
        .get(2..4)
        .ok_or(Error::RealityVerify("truncated CertificateVerify message"))?;
    let sig_len = usize::from(u16::from_be_bytes([sig_len_bytes[0], sig_len_bytes[1]]));
    let sig = body.get(4..4 + sig_len).ok_or(Error::RealityVerify(
        "truncated CertificateVerify signature",
    ))?;
    if sig.is_empty() {
        return Err(Error::RealityVerify("empty CertificateVerify signature"));
    }
    Ok(sig.to_vec())
}

/// Extracts the P-256 subject public key from a leaf certificate's DER (RFC
/// 5280 `Certificate` → `TBSCertificate` → `SubjectPublicKeyInfo`), returned
/// as uncompressed SEC1 bytes (`0x04 ‖ X ‖ Y`, 65 bytes) — the same encoding
/// [`auth::derive_cert_key`]'s `public_sec1` uses, so the two can be
/// compared byte-for-byte.
///
/// Walks the DER with the `der` crate's bounds-checked, panic-free reader
/// (never hand-rolled ASN.1 length arithmetic on untrusted bytes):
/// `TBSCertificate`'s fields are read sequentially — `[0] EXPLICIT Version`
/// is OPTIONAL (default v1), so it is skipped only when present, otherwise
/// the field just peeked at IS `serialNumber` — up through
/// `subjectPublicKeyInfo`, whose raw TLV is handed to
/// [`p256::ecdsa::VerifyingKey::from_public_key_der`]. That call itself
/// fail-closes on "not an EC key", "not the P-256 curve OID", and "not a
/// valid point on the curve" — this function does not duplicate any of
/// those checks.
fn spki_sec1_pubkey_from_der(cert_der: &[u8]) -> Result<Vec<u8>, Error> {
    use der::{AnyRef, Decode, Reader, SliceReader, Tag, Tagged};
    use p256::pkcs8::DecodePublicKey;

    let bad_der = || Error::RealityVerify("leaf certificate is not valid DER");

    let mut top = SliceReader::new(cert_der).map_err(|_| bad_der())?;
    let cert_any = AnyRef::decode(&mut top).map_err(|_| bad_der())?;
    if cert_any.tag() != Tag::Sequence {
        return Err(bad_der());
    }

    let mut cert_body = SliceReader::new(cert_any.value()).map_err(|_| bad_der())?;
    let tbs_any = AnyRef::decode(&mut cert_body).map_err(|_| bad_der())?;
    if tbs_any.tag() != Tag::Sequence {
        return Err(bad_der());
    }

    let mut tbs = SliceReader::new(tbs_any.value()).map_err(|_| bad_der())?;

    // version [0] EXPLICIT is OPTIONAL; if present, consume+discard it — the
    // NEXT field is then serialNumber. If absent, the field already peeked
    // at (still unconsumed) IS serialNumber.
    let first_tag = tbs.peek_tag().map_err(|_| bad_der())?;
    if first_tag.is_context_specific() {
        tbs.tlv_bytes().map_err(|_| bad_der())?; // version
    }
    tbs.tlv_bytes().map_err(|_| bad_der())?; // serialNumber
    tbs.tlv_bytes().map_err(|_| bad_der())?; // signature AlgorithmIdentifier
    tbs.tlv_bytes().map_err(|_| bad_der())?; // issuer
    tbs.tlv_bytes().map_err(|_| bad_der())?; // validity
    tbs.tlv_bytes().map_err(|_| bad_der())?; // subject
    let spki_der = tbs.tlv_bytes().map_err(|_| bad_der())?; // subjectPublicKeyInfo

    let vk = p256::ecdsa::VerifyingKey::from_public_key_der(spki_der).map_err(|_| {
        Error::RealityVerify("leaf certificate's public key is not a valid P-256 SPKI")
    })?;
    Ok(vk.to_encoded_point(false).as_bytes().to_vec())
}

/// Verify a TLS 1.3 server `CertificateVerify` for REALITY.4b: the leaf's
/// key must be exactly `expected_pubkey_sec1` (the shared-secret-derived
/// P-256 key — see [`auth::derive_cert_key`]), and the signature must verify
/// over the RFC 8446 §4.4.3 signed content with `ecdsa_secp256r1_sha256`
/// HARD-PINNED — the `SignatureScheme` the server's `CertificateVerify`
/// message itself announces is never read or trusted. Fail-closed on every
/// edge: a leaf-key mismatch, a malformed DER signature, or a signature
/// that does not verify are all `Err(Error::RealityVerify(_))`, never `Ok`.
fn verify_certificate_verify(
    expected_pubkey_sec1: &[u8],
    leaf_spki_pubkey_sec1: &[u8],
    transcript_hash_through_cert: &[u8],
    signature_der: &[u8],
) -> Result<(), Error> {
    use p256::ecdsa::{signature::Verifier, Signature, VerifyingKey};

    // 1. Pin the leaf key to the derived key (byte comparison — the derived
    // key is always a valid P-256 point by construction, so an exact match
    // also proves the leaf key is one).
    if leaf_spki_pubkey_sec1 != expected_pubkey_sec1 {
        return Err(Error::RealityVerify(
            "leaf key does not match the derived REALITY key",
        ));
    }

    // 2. Reconstruct the §4.4.3 signed content: 0x20*64 ‖ context ‖ 0x00 ‖ hash.
    let mut signed = Vec::with_capacity(64 + 34 + 1 + transcript_hash_through_cert.len());
    signed.extend_from_slice(&[0x20u8; 64]);
    signed.extend_from_slice(b"TLS 1.3, server CertificateVerify");
    signed.push(0x00);
    signed.extend_from_slice(transcript_hash_through_cert);

    // 3. Verify with ecdsa_secp256r1_sha256 hard-pinned (`Verifier::verify`
    // for `ecdsa::Signature<NistP256>` hashes SHA-256 internally, matching
    // ecdsa_secp256r1_sha256 exactly; the announced SignatureScheme from the
    // wire is never consulted).
    let vk = VerifyingKey::from_sec1_bytes(expected_pubkey_sec1)
        .map_err(|_| Error::RealityVerify("derived key is not a valid P-256 point"))?;
    let sig = Signature::from_der(signature_der)
        .map_err(|_| Error::RealityVerify("CertificateVerify signature is not valid DER ECDSA"))?;
    vk.verify(&signed, &sig)
        .map_err(|_| Error::RealityVerify("CertificateVerify signature does not verify"))
}

/// The signing counterpart of [`verify_certificate_verify`]: sign the TLS 1.3
/// server `CertificateVerify` (RFC 8446 §4.4.3) with the derived ECDSA-P256
/// key `signing_key` (from `auth::derive_cert_key(shared)`), over the SAME
/// signed content the verifier reconstructs. This IS the REALITY.4b binding,
/// server side — the client pins the presented leaf's SPKI to this key and
/// verifies this signature. `ecdsa_secp256r1_sha256` (scheme 0x0403) is fixed:
/// `p256::ecdsa::SigningKey` signs SHA-256 internally, so no `suite` is needed
/// (the caller uses `suite` only to compute `transcript_hash_through_certificate`).
pub fn sign_certificate_verify(
    signing_key: &p256::ecdsa::SigningKey,
    transcript_hash_through_certificate: &[u8],
) -> Result<Vec<u8>, Error> {
    use p256::ecdsa::{signature::Signer, Signature};

    let mut signed = Vec::with_capacity(64 + 33 + 1 + transcript_hash_through_certificate.len());
    signed.extend_from_slice(&[0x20u8; 64]);
    signed.extend_from_slice(b"TLS 1.3, server CertificateVerify");
    signed.push(0x00);
    signed.extend_from_slice(transcript_hash_through_certificate);

    let sig: Signature = signing_key
        .try_sign(&signed)
        .map_err(|_| Error::Handshake(handshake::Error::Crypto))?;
    Ok(sig.to_der().as_bytes().to_vec())
}

/// Orchestrates REALITY.4b `CertificateVerify` verification: locates the
/// Certificate/CertificateVerify messages in `hs_flight` (see
/// [`scan_cert_flight`]), extracts the leaf's P-256 SPKI (see
/// [`spki_sec1_pubkey_from_der`]), computes `Transcript-Hash(ClientHello‥
/// Certificate)` at the exact RFC 8446 §4.4.3 boundary `scan_cert_flight`
/// reports, and checks it against `expected_pubkey_sec1` (see
/// [`verify_certificate_verify`]). `ch_sh_transcript` is the raw
/// `ClientHello ‖ ServerHello` bytes `connect` already accumulates —
/// `Transcript-Hash(ClientHello‥Certificate)` is `transcript_hash` of that
/// prefix concatenated with `hs_flight` up to (not including)
/// `certverify_start`.
fn verify_reality_binding(
    hs_flight: &[u8],
    ch_sh_transcript: &[u8],
    suite: u16,
    expected_pubkey_sec1: &[u8],
) -> Result<(), Error> {
    let flight = scan_cert_flight(hs_flight)?;
    let leaf_pubkey_sec1 = spki_sec1_pubkey_from_der(&flight.leaf_der)?;

    let mut thru_cert = Vec::with_capacity(ch_sh_transcript.len() + flight.certverify_start);
    thru_cert.extend_from_slice(ch_sh_transcript);
    thru_cert.extend_from_slice(&hs_flight[..flight.certverify_start]);
    let transcript_thru_cert = transcript_hash(&thru_cert, suite);

    verify_certificate_verify(
        expected_pubkey_sec1,
        &leaf_pubkey_sec1,
        &transcript_thru_cert,
        &flight.signature,
    )
}

/// Best-effort: seals `[fatal, description]` as an encrypted TLS 1.3 alert
/// record under the CLIENT handshake traffic keys (sequence 0 — no prior
/// client-side handshake-key-sealed record has been sent at the point
/// REALITY.4b verification can fail, since it runs before the client
/// `Finished` is sealed/sent) and writes it to `stream`. A seal or write
/// failure on this already-failing path is silently ignored — the caller is
/// about to return the real `Error::RealityVerify` regardless, and a peer
/// that never gets the alert just sees the connection drop, same as any
/// other network failure.
async fn send_alert<S: AsyncWrite + Unpin>(
    stream: &mut S,
    hk: &HandshakeKeys,
    suite: u16,
    description: u8,
) {
    let alert_msg = [ALERT_LEVEL_FATAL, description];
    let Ok(sealed) = record_seal(
        &hk.client_key,
        &hk.client_iv,
        0,
        suite,
        CONTENT_TYPE_ALERT,
        &alert_msg,
    ) else {
        return;
    };
    let Ok(header) = record_header(
        CONTENT_TYPE_APPLICATION_DATA,
        LEGACY_RECORD_VERSION,
        sealed.len(),
    ) else {
        return;
    };
    let _ = stream.write_all(&header).await;
    let _ = stream.write_all(&sealed).await;
}

/// Performs a full RFC 8446 §2 one-round-trip TLS 1.3 client handshake to
/// `stream`, carrying a REALITY auth seal (keyed by `server_reality_pub` and
/// `short_id`) in the crafted Chrome-150 `ClientHello`'s `legacy_session_id`.
/// On success, returns a [`RealityStream`] over the negotiated application
/// traffic keys.
///
/// When `verify` is `false` (byte-identical to this function's pre-
/// REALITY.4b behavior — the JA3/JA4 wire fingerprint is unaffected either
/// way, since verification never changes anything this client SENDS, only
/// what it does with what it receives): does not verify the server's
/// certificate chain, `CertificateVerify`, or `Finished` contents —
/// REALITY's camouflage design means the server on the other end of a
/// *successful* connection presents the real target site's certificate,
/// which this client has no way (and no need) to validate; a misconfigured
/// or non-REALITY-aware peer simply fails to open the auth seal
/// server-side and this handshake either times out or is rejected
/// upstream, not here.
///
/// When `verify` is `true` (REALITY.4b): after the server's
/// EncryptedExtensions/Certificate/CertificateVerify/Finished flight is
/// fully received, ADDITIONALLY pins the Certificate leaf's P-256 key to
/// `auth::derive_cert_key(auth::shared_secret(server_reality_pub,
/// &eph_priv)).public_sec1` and verifies `CertificateVerify` over it (see
/// [`verify_reality_binding`]) — proof the peer holds
/// `server_reality_pub`'s private key, not merely that it can answer a
/// `ClientHello`. On failure, best-effort sends a `bad_certificate` TLS
/// alert (see [`send_alert`]) and returns `Err(Error::RealityVerify(_))`
/// BEFORE the client `Finished` is sent — this client never completes a
/// handshake with, nor sends any encrypted client bytes to, a peer that
/// fails this check.
pub async fn connect<S: AsyncRead + AsyncWrite + Unpin>(
    mut stream: S,
    sni: &str,
    server_reality_pub: &[u8; 32],
    short_id: [u8; 8],
    verify: bool,
) -> Result<RealityStream<S>, Error> {
    // THE ONE EPHEMERAL KEY: raw bytes from getrandom, reused via
    // x25519-dalek for both the TLS key_share/ECDHE and the REALITY auth
    // seal's ECDH. Never ring::agreement — its private key isn't
    // extractable, so it could not be reused for the seal.
    let mut eph_priv = [0u8; 32];
    getrandom::getrandom(&mut eph_priv)?;
    let secret = x25519_dalek::StaticSecret::from(eph_priv);
    let eph_pub: [u8; 32] = x25519_dalek::PublicKey::from(&secret).to_bytes();

    // A genuine ML-KEM-768 keypair (REALITY.2 Task 9), generated fresh per
    // connection like the X25519 ephemeral key above. Real Chrome sends
    // `X25519MLKEM768` (group 4588) as its PRIMARY key_share, and real
    // ML-KEM-strict servers (Cloudflare, Google) validate the encapsulation
    // key's canonical encoding — only a genuine key survives that check (see
    // `hello::key_share_body`'s doc comment). `mlkem_dk` is kept to complete
    // the hybrid exchange if the server selects group 4588 below; `mlkem_ek`
    // is sent as-is in the ClientHello.
    let mut mlkem_rng = MlKemRng::default();
    let (mlkem_dk, mlkem_ek) = MlKem768::generate(&mut mlkem_rng);
    mlkem_rng.into_result()?;

    let mut client_random = [0u8; 32];
    getrandom::getrandom(&mut client_random)?;

    let ts_min = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .map_err(|_| Error::Clock)?
        .as_secs()
        / 60;

    let legacy_session_id = auth::seal(
        server_reality_pub,
        &eph_priv,
        &client_random,
        short_id,
        ts_min,
    );

    let params = ClientHelloParams {
        sni: sni.to_string(),
        key_share_x25519_pub: eph_pub,
        key_share_mlkem_ek: mlkem_ek.as_bytes().to_vec(),
        legacy_session_id,
        client_random,
    };
    let mut rng = OsRng::default();
    let hello_msg = hello::craft(&params, &mut rng);
    rng.into_result()?;

    let ch_header = record_header(
        CONTENT_TYPE_HANDSHAKE,
        LEGACY_RECORD_VERSION_INITIAL,
        hello_msg.len(),
    )?;
    stream.write_all(&ch_header).await?;
    stream.write_all(&hello_msg).await?;

    // ServerHello is sent plaintext (record type 0x16) — RFC 8446 §5.1.
    let (record_type, sh_msg) = read_raw_record(&mut stream).await?;
    if record_type != CONTENT_TYPE_HANDSHAKE {
        return Err(Error::Protocol(
            "expected a plaintext ServerHello handshake record",
        ));
    }
    let server_hello_info = parse_server_hello(&sh_msg)?;

    let mut ch_sh_transcript = Vec::with_capacity(hello_msg.len() + sh_msg.len());
    ch_sh_transcript.extend_from_slice(&hello_msg);
    ch_sh_transcript.extend_from_slice(&sh_msg);
    let transcript_ch_sh = transcript_hash(&ch_sh_transcript, server_hello_info.suite);

    // The (possibly hybrid) ECDHE shared secret fed to the TLS 1.3 key
    // schedule (RFC 8446 §7.1's "(EC)DHE" input, generalized to `&[u8]` —
    // see `derive_handshake_keys`'s doc comment). Group 29 (plain X25519):
    // 32 bytes, the X25519 ECDH output alone. Group 4588
    // (X25519MLKEM768): 64 bytes, `mlkem_shared_secret(32) ‖
    // x25519_shared_secret(32)` — ML-KEM first, matching the client
    // `key_share` entry's own `mlkem_ek ‖ x25519_pub` byte order (see
    // `hello::key_share_body`), per
    // draft-kwiatkowski-tls-ecdhe-mlkem's hybrid combiner. The server's
    // group-4588 key_share is `mlkem768_ciphertext(1088) ‖
    // x25519_public(32)` — `parse_server_hello` already validated that
    // exact length for this group.
    let ecdhe: Vec<u8> = match server_hello_info.group {
        GROUP_X25519MLKEM768 => {
            let (ct_bytes, server_x25519_pub) = server_hello_info
                .server_key_share
                .split_at(MLKEM768_CIPHERTEXT_LEN);
            let ciphertext = ct_bytes
                .try_into()
                .map_err(|_| handshake::Error::MlKemDecapsulation)?;
            let mlkem_ss = mlkem_dk
                .decapsulate(&ciphertext)
                .map_err(|()| handshake::Error::MlKemDecapsulation)?;
            let server_x25519_pub: [u8; 32] = server_x25519_pub
                .try_into()
                .map_err(|_| handshake::Error::BadKeyShareLength)?;
            let x25519_ss = secret
                .diffie_hellman(&x25519_dalek::PublicKey::from(server_x25519_pub))
                .to_bytes();

            let mut combined = Vec::with_capacity(64);
            combined.extend_from_slice(&mlkem_ss);
            combined.extend_from_slice(&x25519_ss);
            combined
        }
        GROUP_X25519 => {
            let server_x25519_pub: [u8; 32] = server_hello_info
                .server_key_share
                .as_slice()
                .try_into()
                .map_err(|_| handshake::Error::BadKeyShareLength)?;
            secret
                .diffie_hellman(&x25519_dalek::PublicKey::from(server_x25519_pub))
                .to_bytes()
                .to_vec()
        }
        _ => {
            return Err(Error::Protocol(
                "parse_server_hello returned an unexpected key_share group",
            ))
        }
    };
    let hk: HandshakeKeys =
        derive_handshake_keys(&ecdhe, &transcript_ch_sh, server_hello_info.suite);

    // Read the server's EncryptedExtensions/Certificate/CertificateVerify/
    // Finished flight: one or more 0x17 records under the server handshake
    // traffic key, possibly preceded by a middlebox-compatibility CCS.
    let mut hs_flight = Vec::new();
    let mut server_seq = 0u64;
    let finished_end = loop {
        let (record_type, mut payload) = read_raw_record(&mut stream).await?;
        if record_type == CONTENT_TYPE_CHANGE_CIPHER_SPEC {
            continue;
        }
        if record_type != CONTENT_TYPE_APPLICATION_DATA {
            return Err(Error::Protocol(
                "expected an encrypted handshake record in the server flight",
            ));
        }
        let opened = record_open(
            &hk.server_key,
            &hk.server_iv,
            server_seq,
            server_hello_info.suite,
            record_type,
            &mut payload,
        )?;
        server_seq = server_seq
            .checked_add(1)
            .ok_or(Error::Protocol("server handshake sequence overflow"))?;
        hs_flight.extend_from_slice(&opened);
        if hs_flight.len() > MAX_SERVER_FLIGHT_LEN {
            return Err(Error::Protocol(
                "server handshake flight exceeds sane bound",
            ));
        }

        if let Some(end) = find_finished_end(&hs_flight)? {
            break end;
        }
    };
    hs_flight.truncate(finished_end);

    if verify {
        let shared = auth::shared_secret(server_reality_pub, &eph_priv);
        let expected_pubkey_sec1 = auth::derive_cert_key(&shared).public_sec1;
        if let Err(e) = verify_reality_binding(
            &hs_flight,
            &ch_sh_transcript,
            server_hello_info.suite,
            &expected_pubkey_sec1,
        ) {
            send_alert(
                &mut stream,
                &hk,
                server_hello_info.suite,
                ALERT_DESCRIPTION_BAD_CERTIFICATE,
            )
            .await;
            return Err(e);
        }
    }

    let mut full_transcript = ch_sh_transcript;
    full_transcript.extend_from_slice(&hs_flight);
    let transcript_thru_sfin = transcript_hash(&full_transcript, server_hello_info.suite);

    // Client Finished: verify_data = HMAC-Hash(finished_key, transcript),
    // finished_key = HKDF-Expand-Label(client_hs_traffic, "finished", "",
    // Hash.length) — Hash is the suite's key-schedule hash throughout (RFC
    // 8446 §4.4.4/§7.1), so verify_data is 32 bytes for SHA-256-hash suites
    // but 48 bytes for TLS_AES_256_GCM_SHA384 (0x1302); the u24 length
    // prefix below must match that, not be hardcoded.
    let verify_data = finished_verify_data(
        &hk.client_hs_traffic,
        &transcript_thru_sfin,
        server_hello_info.suite,
    );

    let mut client_finished_msg = Vec::with_capacity(4 + verify_data.len());
    client_finished_msg.push(HS_TYPE_FINISHED);
    let vd_len = u32::try_from(verify_data.len())
        .map_err(|_| Error::Protocol("Finished verify_data length exceeds a u24"))?;
    client_finished_msg.extend_from_slice(&vd_len.to_be_bytes()[1..]); // u24
    client_finished_msg.extend_from_slice(&verify_data);

    // Middlebox-compatibility CCS, then the sealed client Finished, matching
    // real Chrome/BoringSSL's wire shape.
    stream.write_all(&CHANGE_CIPHER_SPEC_RECORD).await?;

    let sealed_finished = record_seal(
        &hk.client_key,
        &hk.client_iv,
        0,
        server_hello_info.suite,
        CONTENT_TYPE_HANDSHAKE,
        &client_finished_msg,
    )?;
    let fin_header = record_header(
        CONTENT_TYPE_APPLICATION_DATA,
        LEGACY_RECORD_VERSION,
        sealed_finished.len(),
    )?;
    stream.write_all(&fin_header).await?;
    stream.write_all(&sealed_finished).await?;

    let ak = derive_application_keys(
        &hk.handshake_secret,
        &transcript_thru_sfin,
        server_hello_info.suite,
    );

    Ok(RealityStream::client(stream, ak))
}

/// REALITY.5a Task 3: probes a REAL `dest` — no REALITY seal, since `dest`
/// ignores `legacy_session_id` — and captures its server flight's structural
/// fingerprint: the `ServerHello` shape (ordered extensions, negotiated
/// suite/group), the encrypted flight's per-record ciphertext-payload
/// lengths and per-message plaintext lengths, and the leaf/intermediate
/// certificate DERs. Later REALITY.5 sub-milestones (5b/5c/5d) reproduce this
/// structure on the relay's authed path.
///
/// Crafts the SAME Chrome-150 `ClientHello` [`connect`] would (same
/// [`hello::craft`] call, same fresh X25519 ephemeral + ML-KEM setup —
/// reused byte-for-byte below), except `legacy_session_id` is filled with
/// random bytes instead of a REALITY auth seal: probing the real `dest`
/// needs no `server_reality_pub`/`short_id`, and `dest` never inspects
/// `legacy_session_id`'s contents, so the wire fingerprint an observer sees
/// here matches exactly what [`connect`] sends. Completes the handshake
/// through the server's `Finished`, but sends neither a client `Finished`
/// nor reads any application data afterward — this is a probe, not a
/// connection, so it stops the instant it has what it came for.
///
/// A focused SIBLING of [`connect`]: shares [`hello::craft`], the ephemeral
/// key setup, [`parse_server_hello`]/[`parse_server_hello_shape`],
/// [`derive_handshake_keys`], [`record_open`], [`find_finished_end`], and
/// [`scan_flight_shape`], but DUPLICATES `connect`'s ~15-line encrypted-
/// flight read loop (with one addition: recording each record's pre-decrypt
/// ciphertext-payload length into `record_lengths`) rather than refactoring
/// `connect`'s crux security path to share it — see this module's REALITY.5a
/// Task 3 brief: a probe is not the hot path, so a small, clearly-commented
/// duplication here is safer than touching `connect`. `connect`'s body is
/// byte-for-byte unchanged by this function's existence.
pub async fn capture_dest_flight<S: AsyncRead + AsyncWrite + Unpin>(
    mut stream: S,
    sni: &str,
) -> Result<CapturedFlight, Error> {
    // THE ONE EPHEMERAL KEY, same setup as connect's (see connect's doc
    // comment) — reused for the TLS key_share/ECDHE only, since there is no
    // REALITY seal to also key here.
    let mut eph_priv = [0u8; 32];
    getrandom::getrandom(&mut eph_priv)?;
    let secret = x25519_dalek::StaticSecret::from(eph_priv);
    let eph_pub: [u8; 32] = x25519_dalek::PublicKey::from(&secret).to_bytes();

    // A genuine ML-KEM-768 keypair, exactly as connect's — a real
    // ML-KEM-strict dest (Cloudflare, Google) validates the encapsulation
    // key's canonical encoding, so only a genuine key survives probing it.
    let mut mlkem_rng = MlKemRng::default();
    let (mlkem_dk, mlkem_ek) = MlKem768::generate(&mut mlkem_rng);
    mlkem_rng.into_result()?;

    let mut client_random = [0u8; 32];
    getrandom::getrandom(&mut client_random)?;

    // No REALITY seal: dest ignores legacy_session_id entirely, so a random
    // 32-byte value stands in for it — still wire-shaped exactly like a real
    // seal (hello::craft only cares that it fits, not what's inside).
    let mut legacy_session_id = [0u8; 32];
    getrandom::getrandom(&mut legacy_session_id)?;

    let params = ClientHelloParams {
        sni: sni.to_string(),
        key_share_x25519_pub: eph_pub,
        key_share_mlkem_ek: mlkem_ek.as_bytes().to_vec(),
        legacy_session_id,
        client_random,
    };
    let mut rng = OsRng::default();
    let hello_msg = hello::craft(&params, &mut rng);
    rng.into_result()?;

    let ch_header = record_header(
        CONTENT_TYPE_HANDSHAKE,
        LEGACY_RECORD_VERSION_INITIAL,
        hello_msg.len(),
    )?;
    stream.write_all(&ch_header).await?;
    stream.write_all(&hello_msg).await?;

    // ServerHello is sent plaintext (record type 0x16) — RFC 8446 §5.1.
    let (record_type, sh_msg) = read_raw_record(&mut stream).await?;
    if record_type != CONTENT_TYPE_HANDSHAKE {
        return Err(Error::Protocol(
            "expected a plaintext ServerHello handshake record",
        ));
    }
    // The crypto-relevant fields (suite/group/key_share bytes) via
    // parse_server_hello, and the full structural shape (ordered
    // extensions) via parse_server_hello_shape — both walk the identical
    // bounded header, so neither can disagree with the other about what
    // suite/group was negotiated.
    let server_hello_info = parse_server_hello(&sh_msg)?;
    let server_hello_shape = parse_server_hello_shape(&sh_msg)?;

    let mut ch_sh_transcript = Vec::with_capacity(hello_msg.len() + sh_msg.len());
    ch_sh_transcript.extend_from_slice(&hello_msg);
    ch_sh_transcript.extend_from_slice(&sh_msg);
    let transcript_ch_sh = transcript_hash(&ch_sh_transcript, server_hello_info.suite);

    // The (possibly hybrid) ECDHE shared secret — identical branch to
    // connect's (see connect's doc comment for the group-4588 wire shape).
    let ecdhe: Vec<u8> = match server_hello_info.group {
        GROUP_X25519MLKEM768 => {
            let (ct_bytes, server_x25519_pub) = server_hello_info
                .server_key_share
                .split_at(MLKEM768_CIPHERTEXT_LEN);
            let ciphertext = ct_bytes
                .try_into()
                .map_err(|_| handshake::Error::MlKemDecapsulation)?;
            let mlkem_ss = mlkem_dk
                .decapsulate(&ciphertext)
                .map_err(|()| handshake::Error::MlKemDecapsulation)?;
            let server_x25519_pub: [u8; 32] = server_x25519_pub
                .try_into()
                .map_err(|_| handshake::Error::BadKeyShareLength)?;
            let x25519_ss = secret
                .diffie_hellman(&x25519_dalek::PublicKey::from(server_x25519_pub))
                .to_bytes();

            let mut combined = Vec::with_capacity(64);
            combined.extend_from_slice(&mlkem_ss);
            combined.extend_from_slice(&x25519_ss);
            combined
        }
        GROUP_X25519 => {
            let server_x25519_pub: [u8; 32] = server_hello_info
                .server_key_share
                .as_slice()
                .try_into()
                .map_err(|_| handshake::Error::BadKeyShareLength)?;
            secret
                .diffie_hellman(&x25519_dalek::PublicKey::from(server_x25519_pub))
                .to_bytes()
                .to_vec()
        }
        _ => {
            return Err(Error::Protocol(
                "parse_server_hello returned an unexpected key_share group",
            ))
        }
    };
    let hk: HandshakeKeys =
        derive_handshake_keys(&ecdhe, &transcript_ch_sh, server_hello_info.suite);

    // Read the server's EncryptedExtensions/Certificate/CertificateVerify/
    // Finished flight, recording each record's ciphertext-payload length
    // (the record header's length field, i.e. `payload.len()` BEFORE
    // `record_open` mutates it in place to strip the tag/padding) into
    // `record_lengths` as we go. DELIBERATE DUPLICATE of connect's own
    // flight-read loop (see this function's doc comment) — the only
    // difference is this extra bookkeeping line; the MAX_SERVER_FLIGHT_LEN
    // cap below applies identically, since the flight buffer is attacker-
    // length-controlled here exactly as it is in connect.
    let mut hs_flight = Vec::new();
    let mut record_lengths = Vec::new();
    let mut server_seq = 0u64;
    let finished_end = loop {
        let (record_type, mut payload) = read_raw_record(&mut stream).await?;
        if record_type == CONTENT_TYPE_CHANGE_CIPHER_SPEC {
            continue;
        }
        if record_type != CONTENT_TYPE_APPLICATION_DATA {
            return Err(Error::Protocol(
                "expected an encrypted handshake record in the server flight",
            ));
        }
        record_lengths.push(payload.len());
        let opened = record_open(
            &hk.server_key,
            &hk.server_iv,
            server_seq,
            server_hello_info.suite,
            record_type,
            &mut payload,
        )?;
        server_seq = server_seq
            .checked_add(1)
            .ok_or(Error::Protocol("server handshake sequence overflow"))?;
        hs_flight.extend_from_slice(&opened);
        if hs_flight.len() > MAX_SERVER_FLIGHT_LEN {
            return Err(Error::Protocol(
                "server handshake flight exceeds sane bound",
            ));
        }

        if let Some(end) = find_finished_end(&hs_flight)? {
            break end;
        }
    };
    hs_flight.truncate(finished_end);

    // Do NOT send a client Finished or read application data — the probe
    // has everything it needs the instant the server's Finished is seen.
    let scan = scan_flight_shape(&hs_flight)?;

    Ok(CapturedFlight {
        leaf_der: scan.leaf_der,
        template: ServerFlightTemplate {
            server_hello: server_hello_shape,
            encrypted_flight: EncryptedFlightShape {
                record_lengths,
                encrypted_extensions_len: scan.ee_len,
                certificate_len: scan.cert_len,
                certificate_verify_len: scan.cert_verify_len,
                finished_len: scan.finished_len,
            },
            cert_chain: scan.cert_chain,
        },
    })
}

/// The output of [`emit_server_flight`]: the wire bytes to send (CCS ‖ sealed
/// handshake-key records) and the application traffic keys for the data phase.
pub struct ServerFlight {
    pub wire: Vec<u8>,
    pub app_keys: ApplicationKeys,
}

/// Wrap a handshake-message body as `type ‖ u24 len ‖ body`.
fn handshake_message(msg_type: u8, body: &[u8]) -> Result<Vec<u8>, Error> {
    let len = u32::try_from(body.len()).map_err(|_| handshake::Error::RecordTooLarge)?;
    if body.len() > 0xFF_FFFF {
        return Err(handshake::Error::RecordTooLarge.into());
    }
    let mut out = Vec::with_capacity(4 + body.len());
    out.push(msg_type);
    out.extend_from_slice(&len.to_be_bytes()[1..]); // u24
    out.extend_from_slice(body);
    Ok(out)
}

/// A single `CertificateEntry`: `u24 cert_data_len ‖ der ‖ u16 ext_len(0)`.
fn certificate_entry(der: &[u8]) -> Result<Vec<u8>, Error> {
    let len = u32::try_from(der.len()).map_err(|_| handshake::Error::RecordTooLarge)?;
    if der.len() > 0xFF_FFFF {
        return Err(handshake::Error::RecordTooLarge.into());
    }
    let mut out = Vec::with_capacity(3 + der.len() + 2);
    out.extend_from_slice(&len.to_be_bytes()[1..]); // u24
    out.extend_from_slice(der);
    out.extend_from_slice(&[0x00, 0x00]); // empty entry extensions
    Ok(out)
}

/// REALITY.5c: assemble the encrypted server flight (EncryptedExtensions/
/// Certificate/CertificateVerify/Finished), seal it under `keys`'s handshake
/// keys, and frame it to byte-match `flight_shape.record_lengths` (padding
/// each record to its captured length), prefixed with the middlebox-compat
/// CCS. Returns the wire bytes + the derived application keys. Fail-closed: a
/// malformed or too-small `record_lengths` → `Err` (5d degrades to splice).
pub fn emit_server_flight(
    keys: &HandshakeKeys,
    flight_shape: &crate::template::EncryptedFlightShape,
    cert_chain: &crate::template::CertChainShape,
    forged_leaf_der: &[u8],
    cert_signing_key: &p256::ecdsa::SigningKey,
    transcript_ch_sh: &[u8],
) -> Result<ServerFlight, Error> {
    let suite = keys.suite;

    // 1. EncryptedExtensions: empty extensions list.
    let ee = handshake_message(0x08, &[0x00, 0x00])?;

    // 2. Certificate: empty context ‖ u24 list-len ‖ leaf entry ‖ intermediates.
    let mut cert_list = certificate_entry(forged_leaf_der)?;
    for inter in &cert_chain.intermediates_der {
        cert_list.extend_from_slice(&certificate_entry(inter)?);
    }
    let mut cert_body = Vec::with_capacity(1 + 3 + cert_list.len());
    cert_body.push(0x00); // certificate_request_context length = 0
    let list_len = u32::try_from(cert_list.len()).map_err(|_| handshake::Error::RecordTooLarge)?;
    if cert_list.len() > 0xFF_FFFF {
        return Err(handshake::Error::RecordTooLarge.into());
    }
    cert_body.extend_from_slice(&list_len.to_be_bytes()[1..]); // u24
    cert_body.extend_from_slice(&cert_list);
    let certificate = handshake_message(0x0b, &cert_body)?;

    // 3. CertificateVerify over the transcript through Certificate.
    let mut tr = transcript_ch_sh.to_vec();
    tr.extend_from_slice(&ee);
    tr.extend_from_slice(&certificate);
    let th_cert = transcript_hash(&tr, suite);
    let sig = sign_certificate_verify(cert_signing_key, &th_cert)?;
    let mut cv_body = Vec::with_capacity(2 + 2 + sig.len());
    cv_body.extend_from_slice(&0x0403u16.to_be_bytes()); // ecdsa_secp256r1_sha256
    cv_body.extend_from_slice(
        &u16::try_from(sig.len())
            .map_err(|_| handshake::Error::RecordTooLarge)?
            .to_be_bytes(),
    );
    cv_body.extend_from_slice(&sig);
    let certificate_verify = handshake_message(0x0f, &cv_body)?;

    // 4. Finished over the transcript through CertificateVerify.
    tr.extend_from_slice(&certificate_verify);
    let th_cv = transcript_hash(&tr, suite);
    let verify_data = finished_verify_data(&keys.server_hs_traffic, &th_cv, suite);
    let finished = handshake_message(0x14, &verify_data)?;

    // Application keys over the transcript through the server Finished.
    tr.extend_from_slice(&finished);
    let th_fin = transcript_hash(&tr, suite);
    let app_keys = derive_application_keys(&keys.handshake_secret, &th_fin, suite);

    // The contiguous handshake-message plaintext to frame.
    let mut hs_stream = Vec::with_capacity(
        ee.len() + certificate.len() + certificate_verify.len() + finished.len(),
    );
    hs_stream.extend_from_slice(&ee);
    hs_stream.extend_from_slice(&certificate);
    hs_stream.extend_from_slice(&certificate_verify);
    hs_stream.extend_from_slice(&finished);

    // 5. Greedy record framing to match record_lengths exactly.
    if flight_shape.record_lengths.is_empty() {
        return Err(Error::Protocol(
            "empty record_lengths in captured flight template",
        ));
    }
    let mut wire = CHANGE_CIPHER_SPEC_RECORD.to_vec();
    let mut off = 0usize;
    for (i, &rlen) in flight_shape.record_lengths.iter().enumerate() {
        // cap = plaintext(+padding) budget for this record (excludes content-type + tag).
        let cap = rlen.checked_sub(17).ok_or(Error::Protocol(
            "record length below the 17-byte AEAD floor",
        ))?;
        let remaining = hs_stream.len() - off;
        // The client (`connect`) reads records only until it sees `Finished`, then
        // switches to the application keys. So the `Finished` tail MUST land in the
        // LAST record — otherwise trailing handshake-key padding records would be
        // left unread and then fail to open under the app key. Reserve one content
        // byte for every later record so content is never exhausted early; the last
        // record always receives the tail. (Leading records may still be pure
        // padding — that is fine, they precede `Finished` and the client reads them.)
        let records_after = flight_shape.record_lengths.len() - 1 - i;
        let chunk_len = cap.min(remaining.saturating_sub(records_after));
        let chunk = &hs_stream[off..off + chunk_len];
        let pad_len = cap - chunk_len; // once hs_stream is exhausted, chunk_len=0 → pad_len=cap (pure-padding record)
        let seq = u64::try_from(i).map_err(|_| Error::Protocol("record index overflow"))?;
        let sealed = record_seal_padded(
            &keys.server_key,
            &keys.server_iv,
            seq,
            suite,
            CONTENT_TYPE_HANDSHAKE,
            chunk,
            pad_len,
        )?;
        let hdr = record_header(
            CONTENT_TYPE_APPLICATION_DATA,
            LEGACY_RECORD_VERSION,
            sealed.len(),
        )?;
        wire.extend_from_slice(&hdr);
        wire.extend_from_slice(&sealed);
        off += chunk_len;
    }
    if off < hs_stream.len() {
        // The flight did not fit the captured record framing.
        return Err(Error::FlightTooLarge);
    }

    Ok(ServerFlight { wire, app_keys })
}

/// REALITY.5c server entry point — the mirror of [`connect`]. Emits the
/// encrypted server flight (via [`emit_server_flight`]) framed to `dest`'s
/// captured record lengths, writes it, drains the client's middlebox CCS +
/// `Finished` (contents UNCHECKED — the outer TLS is zero client-auth by
/// design, exactly as `connect` never checks the server's `Finished`), and
/// returns the server-role [`RealityStream`] on the derived application keys.
/// The ServerHello (REALITY.5b `emit_server_hello`) and `transcript_ch_sh`
/// (raw ClientHello ‖ ServerHello) must already have been produced/sent by
/// the caller (5d).
pub async fn serve<S: AsyncRead + AsyncWrite + Unpin>(
    mut stream: S,
    keys: &HandshakeKeys,
    flight_shape: &crate::template::EncryptedFlightShape,
    cert_chain: &crate::template::CertChainShape,
    forged_leaf_der: &[u8],
    cert_signing_key: &p256::ecdsa::SigningKey,
    transcript_ch_sh: &[u8],
) -> Result<RealityStream<S>, Error> {
    let flight = emit_server_flight(
        keys,
        flight_shape,
        cert_chain,
        forged_leaf_der,
        cert_signing_key,
        transcript_ch_sh,
    )?;
    stream.write_all(&flight.wire).await?;

    // Drain the client's CCS + sealed Finished. The client sends a CCS record
    // (skipped) then exactly one application-data record carrying the sealed
    // Finished, opened under the CLIENT handshake key; its contents are not
    // validated (zero client-auth).
    let mut client_hs_seq = 0u64;
    loop {
        let (record_type, mut payload) = read_raw_record(&mut stream).await?;
        if record_type == CONTENT_TYPE_CHANGE_CIPHER_SPEC {
            continue;
        }
        if record_type != CONTENT_TYPE_APPLICATION_DATA {
            return Err(Error::Protocol(
                "expected the client's sealed Finished record",
            ));
        }
        // Open (and discard) the client Finished — proves it is well-framed
        // under the negotiated key; contents intentionally unchecked.
        record_open(
            &keys.client_key,
            &keys.client_iv,
            client_hs_seq,
            keys.suite,
            record_type,
            &mut payload,
        )?;
        #[expect(
            unused_assignments,
            reason = "written for symmetry with connect()'s analogous server_seq counter \
                      (and to fail closed on overflow before the break); this loop always \
                      exits after the client's single Finished record, so the new value is \
                      never read back"
        )]
        {
            client_hs_seq = client_hs_seq
                .checked_add(1)
                .ok_or(Error::Protocol("client handshake sequence overflow"))?;
        }
        break;
    }

    Ok(RealityStream::server(stream, flight.app_keys))
}

/// An established REALITY/TLS 1.3 connection's application-data stream.
/// Implements `AsyncRead`/`AsyncWrite` over the negotiated application
/// traffic keys, framing plaintext into/out of TLS 1.3 records
/// (RFC 8446 §5.2) with independent per-direction sequence counters. Key
/// update (RFC 8446 §4.6.3) is not implemented — out of scope for the MVP;
/// a long-lived stream that exhausts its 64-bit sequence counter fails
/// closed rather than reusing a nonce.
pub struct RealityStream<S> {
    inner: S,
    /// The negotiated TLS 1.3 cipher suite (`0x1301`/`0x1302`/`0x1303`) —
    /// needed on every seal/open, since `TLS_AES_256_GCM_SHA384` and
    /// `TLS_CHACHA20_POLY1305_SHA256` share a 32-byte key length and so
    /// cannot be told apart from `egress_key`/`ingress_key` alone.
    suite: u16,
    egress_key: Vec<u8>,
    egress_iv: [u8; 12],
    egress_seq: u64,
    ingress_key: Vec<u8>,
    ingress_iv: [u8; 12],
    ingress_seq: u64,
    /// Raw bytes read from `inner` that have not yet been assembled into a
    /// complete TLS record.
    raw_buf: Vec<u8>,
    /// Decrypted application-data plaintext not yet returned to the
    /// `AsyncRead` caller.
    read_buf: Vec<u8>,
    read_pos: usize,
    /// A fully-sealed outgoing record not yet fully written to `inner`.
    write_buf: Vec<u8>,
    write_off: usize,
}

fn protocol_io_error(msg: &'static str) -> io::Error {
    io::Error::new(io::ErrorKind::InvalidData, msg)
}

fn protocol_io_error_owned(msg: String) -> io::Error {
    io::Error::new(io::ErrorKind::InvalidData, msg)
}

fn handshake_io_error(e: handshake::Error) -> io::Error {
    io::Error::new(io::ErrorKind::InvalidData, e.to_string())
}

impl<S: AsyncRead + AsyncWrite + Unpin> RealityStream<S> {
    /// Build the CLIENT-role stream (used by [`connect`]): egress sealed with
    /// the client application key, ingress opened with the server's.
    fn client(inner: S, ak: handshake::ApplicationKeys) -> Self {
        RealityStream {
            inner,
            suite: ak.suite,
            egress_key: ak.client_key,
            egress_iv: ak.client_iv,
            egress_seq: 0,
            ingress_key: ak.server_key,
            ingress_iv: ak.server_iv,
            ingress_seq: 0,
            raw_buf: Vec::new(),
            read_buf: Vec::new(),
            read_pos: 0,
            write_buf: Vec::new(),
            write_off: 0,
        }
    }

    /// Build the SERVER-role stream (used by [`serve`]): egress sealed with the
    /// server application key, ingress opened with the client's — the mirror of
    /// [`client`].
    fn server(inner: S, ak: handshake::ApplicationKeys) -> Self {
        RealityStream {
            inner,
            suite: ak.suite,
            egress_key: ak.server_key,
            egress_iv: ak.server_iv,
            egress_seq: 0,
            ingress_key: ak.client_key,
            ingress_iv: ak.client_iv,
            ingress_seq: 0,
            raw_buf: Vec::new(),
            read_buf: Vec::new(),
            read_pos: 0,
            write_buf: Vec::new(),
            write_off: 0,
        }
    }

    /// Attempts to decrypt and buffer one more application-data record into
    /// `self.read_buf`, transparently skipping `ChangeCipherSpec` records and
    /// post-handshake handshake-typed records (e.g. `NewSessionTicket`)
    /// interleaved with application data. Returns `Ready(Ok(()))` once at
    /// least one byte of application data has been buffered (or the peer
    /// cleanly closed, in which case `read_buf` is left unchanged and a
    /// subsequent `poll_read` call reports EOF via `Ok(())` with `buf`
    /// untouched).
    fn poll_fill_read_buf(&mut self, cx: &mut Context<'_>) -> Poll<io::Result<()>> {
        loop {
            while self.raw_buf.len() >= RECORD_HEADER_LEN {
                let len = usize::from(u16::from_be_bytes([self.raw_buf[3], self.raw_buf[4]]));
                let total = RECORD_HEADER_LEN + len;
                if self.raw_buf.len() < total {
                    break;
                }
                let record_type = self.raw_buf[0];
                let mut payload = self.raw_buf[RECORD_HEADER_LEN..total].to_vec();
                self.raw_buf.drain(..total);

                if record_type == CONTENT_TYPE_CHANGE_CIPHER_SPEC {
                    continue;
                }
                if record_type != CONTENT_TYPE_APPLICATION_DATA {
                    return Poll::Ready(Err(protocol_io_error(
                        "unexpected outer record type in application-data stream",
                    )));
                }

                let (inner_type, content) = record_open_typed(
                    &self.ingress_key,
                    &self.ingress_iv,
                    self.ingress_seq,
                    self.suite,
                    record_type,
                    &mut payload,
                )
                .map_err(handshake_io_error)?;
                self.ingress_seq = self.ingress_seq.checked_add(1).ok_or_else(|| {
                    protocol_io_error("ingress application sequence counter overflow")
                })?;

                if inner_type == CONTENT_TYPE_APPLICATION_DATA {
                    self.read_buf.extend_from_slice(&content);
                    return Poll::Ready(Ok(()));
                }
                if inner_type == CONTENT_TYPE_ALERT {
                    // RFC 8446 §5.2/§6.1: post-handshake alerts arrive
                    // encrypted, inside TLSInnerPlaintext, exactly like
                    // handshake/application-data content — never as a
                    // distinct outer record type. `content` is
                    // `level(1) || description(1)`.
                    let description = content.get(1).copied();
                    return Poll::Ready(match description {
                        Some(ALERT_DESCRIPTION_CLOSE_NOTIFY) => Ok(()), // clean EOF
                        Some(d) => Err(protocol_io_error_owned(format!(
                            "peer sent a fatal TLS alert (description {d}) instead of application data"
                        ))),
                        None => Err(protocol_io_error("peer sent a truncated TLS alert record")),
                    });
                }
                // Inner type is handshake (e.g. a post-handshake
                // NewSessionTicket) — consumed and ignored, look for the
                // next already-buffered record.
            }

            let mut tmp = [0u8; 4096];
            let mut read_buf = ReadBuf::new(&mut tmp);
            match Pin::new(&mut self.inner).poll_read(cx, &mut read_buf) {
                Poll::Ready(Ok(())) => {
                    let n = read_buf.filled().len();
                    if n == 0 {
                        if self.raw_buf.is_empty() {
                            // Clean EOF at a record boundary.
                            return Poll::Ready(Ok(()));
                        }
                        return Poll::Ready(Err(io::Error::new(
                            io::ErrorKind::UnexpectedEof,
                            "peer closed mid-record",
                        )));
                    }
                    self.raw_buf.extend_from_slice(read_buf.filled());
                }
                Poll::Ready(Err(e)) => return Poll::Ready(Err(e)),
                Poll::Pending => return Poll::Pending,
            }
        }
    }
}

impl<S: AsyncRead + AsyncWrite + Unpin> AsyncRead for RealityStream<S> {
    fn poll_read(
        self: Pin<&mut Self>,
        cx: &mut Context<'_>,
        buf: &mut ReadBuf<'_>,
    ) -> Poll<io::Result<()>> {
        let this = self.get_mut();
        loop {
            if this.read_pos < this.read_buf.len() {
                let available = this.read_buf.len() - this.read_pos;
                let n = available.min(buf.remaining());
                buf.put_slice(&this.read_buf[this.read_pos..this.read_pos + n]);
                this.read_pos += n;
                if this.read_pos == this.read_buf.len() {
                    this.read_buf.clear();
                    this.read_pos = 0;
                }
                return Poll::Ready(Ok(()));
            }

            let before = this.read_buf.len();
            match this.poll_fill_read_buf(cx) {
                Poll::Ready(Ok(())) => {
                    if this.read_buf.len() == before {
                        // Clean EOF: no new data was buffered.
                        return Poll::Ready(Ok(()));
                    }
                    // New application data buffered; loop to copy it out.
                }
                Poll::Ready(Err(e)) => return Poll::Ready(Err(e)),
                Poll::Pending => return Poll::Pending,
            }
        }
    }
}

impl<S: AsyncRead + AsyncWrite + Unpin> AsyncWrite for RealityStream<S> {
    fn poll_write(
        self: Pin<&mut Self>,
        cx: &mut Context<'_>,
        buf: &[u8],
    ) -> Poll<io::Result<usize>> {
        let this = self.get_mut();

        // Drain any previously sealed-but-unsent record before accepting
        // more application-level bytes, to preserve wire ordering.
        while this.write_off < this.write_buf.len() {
            match Pin::new(&mut this.inner).poll_write(cx, &this.write_buf[this.write_off..]) {
                Poll::Ready(Ok(0)) => {
                    return Poll::Ready(Err(io::Error::new(
                        io::ErrorKind::WriteZero,
                        "write zero",
                    )));
                }
                Poll::Ready(Ok(n)) => this.write_off += n,
                Poll::Ready(Err(e)) => return Poll::Ready(Err(e)),
                Poll::Pending => return Poll::Pending,
            }
        }

        if buf.is_empty() {
            return Poll::Ready(Ok(0));
        }

        let chunk_len = buf.len().min(MAX_APP_RECORD_LEN);
        let chunk = &buf[..chunk_len];
        let sealed = record_seal(
            &this.egress_key,
            &this.egress_iv,
            this.egress_seq,
            this.suite,
            CONTENT_TYPE_APPLICATION_DATA,
            chunk,
        )
        .map_err(handshake_io_error)?;
        this.egress_seq = this
            .egress_seq
            .checked_add(1)
            .ok_or_else(|| protocol_io_error("egress application sequence counter overflow"))?;
        let header = record_header(
            CONTENT_TYPE_APPLICATION_DATA,
            LEGACY_RECORD_VERSION,
            sealed.len(),
        )
        .map_err(|e| io::Error::new(io::ErrorKind::InvalidInput, e.to_string()))?;

        let mut record = Vec::with_capacity(RECORD_HEADER_LEN + sealed.len());
        record.extend_from_slice(&header);
        record.extend_from_slice(&sealed);
        this.write_buf = record;
        this.write_off = 0;

        while this.write_off < this.write_buf.len() {
            match Pin::new(&mut this.inner).poll_write(cx, &this.write_buf[this.write_off..]) {
                Poll::Ready(Ok(0)) => {
                    return Poll::Ready(Err(io::Error::new(
                        io::ErrorKind::WriteZero,
                        "write zero",
                    )));
                }
                Poll::Ready(Ok(n)) => this.write_off += n,
                Poll::Ready(Err(e)) => return Poll::Ready(Err(e)),
                Poll::Pending => break, // remainder stays buffered for the next poll
            }
        }

        Poll::Ready(Ok(chunk_len))
    }

    fn poll_flush(self: Pin<&mut Self>, cx: &mut Context<'_>) -> Poll<io::Result<()>> {
        let this = self.get_mut();
        while this.write_off < this.write_buf.len() {
            match Pin::new(&mut this.inner).poll_write(cx, &this.write_buf[this.write_off..]) {
                Poll::Ready(Ok(0)) => {
                    return Poll::Ready(Err(io::Error::new(
                        io::ErrorKind::WriteZero,
                        "write zero",
                    )));
                }
                Poll::Ready(Ok(n)) => this.write_off += n,
                Poll::Ready(Err(e)) => return Poll::Ready(Err(e)),
                Poll::Pending => return Poll::Pending,
            }
        }
        Pin::new(&mut this.inner).poll_flush(cx)
    }

    fn poll_shutdown(self: Pin<&mut Self>, cx: &mut Context<'_>) -> Poll<io::Result<()>> {
        let this = self.get_mut();
        while this.write_off < this.write_buf.len() {
            match Pin::new(&mut this.inner).poll_write(cx, &this.write_buf[this.write_off..]) {
                Poll::Ready(Ok(0)) => {
                    return Poll::Ready(Err(io::Error::new(
                        io::ErrorKind::WriteZero,
                        "write zero",
                    )));
                }
                Poll::Ready(Ok(n)) => this.write_off += n,
                Poll::Ready(Err(e)) => return Poll::Ready(Err(e)),
                Poll::Pending => return Poll::Pending,
            }
        }
        Pin::new(&mut this.inner).poll_shutdown(cx)
    }
}

