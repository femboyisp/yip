        let keys = derive_handshake_keys(&ecdhe, &th, SUITE_AES_128_GCM_SHA256);
        let signing_key = p256::ecdsa::SigningKey::from_slice(&[0x33u8; 32]).unwrap();
        // A real leaf whose key is the signing key (test-only helper already used
